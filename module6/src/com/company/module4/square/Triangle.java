package com.company.module4.square;

public class Triangle {
    private int oneSide;
    private int height;

    public Triangle(int oneSide, int height) {
        this.oneSide = oneSide;
        this.height = height;
    }

    public int getOneSide() {
        return oneSide;
    }

    public int getHeight() {
        return height;
    }
}
